"""HTML renderer (SDLC-064, rewritten for v0.9.0 — SDLC-069).

The v0.8.0 renderer mirrored the Markdown structure section-for-section.
That was a shortcut. The v0.9.0 renderer is structured as a diligence-grade
report:

1. **Header band** with archetype, profile, and analysis timestamp.
2. **Executive callout** — the bottom-line for the chosen persona, leading
   with three persona-specific facts (e.g. "Bus factor of 1, no lockfile,
   12 critical blockers" for an acquisition-diligence audience).
3. **Scorecard** — single-screen card grid with score, verdict, blocker
   counts, and finding counts (production vs fixture-derived, segregated).
4. **Persona narrative sections** — one section per
   ``narrative_emphasis`` term in the use-case profile. Generated by the
   ``narrative_blocks`` builders (SDLC-068), so the same data drives both
   the Markdown and HTML outputs.
5. **Hard blockers** — pre-action callout box listing each one with
   closure requirements.
6. **Top risks** — top-5 production findings (fixture findings in §10.2).
7. **Quantitative inventory** — definition list.
8. **Category scoring matrix** — sortable table.
9. **Detailed findings** — split into 9.1 (production, sortable) and
   9.2 (fixture / non-production, collapsed by default).
10. **Evidence appendix** — finding count.

The page uses an embedded stylesheet, an embedded sort-script, and works
offline. Print stylesheet collapses sortable headers and reflows for A4.
"""

