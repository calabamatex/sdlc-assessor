def _esc(value: object) -> str:
    return html.escape(str(value), quote=True) if value is not None else ""


def _normalize_category_scores(raw: object) -> list[dict]:
    if isinstance(raw, list):
        return [r for r in raw if isinstance(r, dict)]
    if isinstance(raw, dict):
        warnings.warn(
            "scoring.category_scores is in the legacy dict shape; converting "
            "for HTML rendering.",
            DeprecationWarning,
            stacklevel=2,
        )
        out: list[dict] = []
        for cat, data in raw.items():
            applicability = data.get("applicability", "applicable")
            out.append(
                {
                    "category": cat,
                    "applicable": applicability != "not_applicable",
                    "score": int(round(data.get("score", 0))),
                    "max_score": int(round(data.get("max", data.get("max_score", 0)))),
                    "summary": data.get("summary", ""),
                    "key_findings": data.get("key_findings", []),
                }
            )
        return out
    return []


def _finding_rank(f: dict) -> float:
    sev = _SEVERITY_RANK.get(f.get("severity", "low"), 0)
    conf = _CONFIDENCE_RANK.get(f.get("confidence", "medium"), 0.0)
    mag = float(f.get("score_impact", {}).get("magnitude", 0)) / 10.0
    return sev * conf * mag


def _split_findings(findings: list[dict]) -> tuple[list[dict], list[dict]]:
    production: list[dict] = []
    fixture: list[dict] = []
    for f in findings:
        if is_fixture_finding(f):
            fixture.append(f)
        else:
            production.append(f)
    return production, fixture


def _resolve_use_case_profile(use_case: str | None) -> dict | None:
    if not use_case:
        return None
    try:
        all_profiles = load_use_case_profiles()
    except Exception:
        return None
    return all_profiles.get(use_case) or None


def _exec_callout_html(scored: dict, blocks: list[NarrativeBlock]) -> str:
    scoring = scored.get("scoring") or {}
    classification = scored.get("classification") or {}
    overall = scoring.get("overall_score", "n/a")
    verdict = scoring.get("verdict", "n/a")
    archetype = classification.get("repo_archetype", "unknown")
    crit = sum(1 for b in scored.get("hard_blockers") or [] if b.get("severity") == "critical")
    high = sum(1 for b in scored.get("hard_blockers") or [] if b.get("severity") == "high")

    headline_parts: list[str] = []
    headline_parts.append(
        f"Overall <strong>{_esc(overall)}/100</strong>"
        f" — verdict <span class=\"verdict-pill verdict-{_esc(verdict)}\">{_esc(verdict)}</span>"
    )
    if crit or high:
        sev_summary: list[str] = []
        if crit:
            sev_summary.append(f"<strong>{crit}</strong> critical")
        if high:
            sev_summary.append(f"<strong>{high}</strong> high")
        headline_parts.append(f"with {' and '.join(sev_summary)} blocker{'s' if (crit + high) != 1 else ''}")
    else:
        headline_parts.append("with no hard blockers")
    headline_parts.append(f"on a {_esc(archetype)} archetype")
    headline = ", ".join(headline_parts) + "."

    # Pull the first 1–2 narrative summaries to form the lede prose.
    lede_html = ""
    if blocks:
        first = blocks[0]
        lede_html = f'<p class="lede">{_esc(first.summary)}</p>'

    # Pull a short bulleted list of the strongest callouts across all blocks.
    callouts: list[tuple[str, str]] = []
    for b in blocks:
        for c in b.callouts:
            callouts.append((c.severity, c.message))
    # De-dupe and pick top 4 by severity rank.
    seen: set[str] = set()
    sorted_callouts: list[tuple[str, str]] = []
    for sev, msg in sorted(
        callouts, key=lambda t: -_SEVERITY_RANK.get(t[0], 0)
    ):
        if msg in seen:
            continue
        seen.add(msg)
        sorted_callouts.append((sev, msg))
        if len(sorted_callouts) >= 4:
            break
    bullets_html = ""
    if sorted_callouts:
        items = "".join(
            f'<li><span class="verdict-pill verdict-{_esc(sev)}">{_esc(sev)}</span> {_esc(msg)}</li>'
            for sev, msg in sorted_callouts
        )
        bullets_html = f"<ul>{items}</ul>"

    return (
        '<aside class="exec-callout" aria-label="Executive summary">'
        '<h2>Executive summary</h2>'
        f'<p class="headline">{headline}</p>'
        f"{lede_html}"
        f"{bullets_html}"
        "</aside>"
    )


def _scorecard_html(scored: dict, *, prod_findings: int, fixture_count: int) -> str:
    scoring = scored.get("scoring") or {}
    overall = scoring.get("overall_score", "n/a")
    verdict = scoring.get("verdict", "n/a")
    confidence = scoring.get("score_confidence", "n/a")
    blockers = scored.get("hard_blockers") or []
    crit = sum(1 for b in blockers if b.get("severity") == "critical")
    high = sum(1 for b in blockers if b.get("severity") == "high")
    classification = scored.get("classification") or {}

    cards = [
        ("Overall", str(_esc(overall)), "of 100"),
        (
            "Verdict",
            f'<span class="verdict-pill verdict-{_esc(verdict)}">{_esc(verdict)}</span>',
            f"score confidence: {_esc(confidence)}",
        ),
        (
            "Hard blockers",
            f"{crit + high}",
            f"{crit} critical · {high} high",
        ),
        (
            "Production findings",
            str(prod_findings),
            f"+ {fixture_count} fixture-derived (segregated)",
        ),
        (
            "Archetype",
            _esc(classification.get("repo_archetype", "unknown")),
            f"maturity: {_esc(classification.get('maturity_profile', 'unknown'))}",
        ),
        (
            "Classification confidence",
            f"{float(classification.get('classification_confidence') or 0.0):.2f}",
            "of 1.0",
        ),
    ]
    cards_html = "".join(
        f'<div class="score-card">'
        f'<div class="label">{label}</div>'
        f'<div class="value">{value}</div>'
        f'<div class="sublabel">{sublabel}</div>'
        f"</div>"
        for label, value, sublabel in cards
    )
    return f'<div class="scorecard">{cards_html}</div>'


def _narrative_block_html(block: NarrativeBlock) -> str:
    facts_html = ""
    if block.facts:
        rows = "".join(
            f'<div class="fact{(" sev-" + _esc(fact.severity)) if fact.severity else ""}">'
            f'<span class="label">{_esc(fact.label)}</span>'
            f'<span class="value">{_esc(fact.value)}</span>'
            f"</div>"
            for fact in block.facts
        )
        facts_html = f'<div class="facts">{rows}</div>'

    callouts_html = ""
    if block.callouts:
        lines = "".join(
            f'<div class="callout callout-{_esc(c.severity)}">{_esc(c.message)}</div>'
            for c in block.callouts
        )
        callouts_html = f'<div class="callouts">{lines}</div>'

    return (
        '<article class="narrative-block">'
        f"<h3>{_esc(block.title)}</h3>"
        f'<p class="summary">{_esc(block.summary)}</p>'
        f"{facts_html}"
        f"{callouts_html}"
        "</article>"
    )


def _hard_blockers_html(scored: dict) -> str:
    blockers = scored.get("hard_blockers") or []
    if not blockers:
        return (
            '<section class="report-section">'
            "<h2>Hard blockers</h2>"
            '<p class="muted">No hard blockers were triggered.</p>'
            "</section>"
        )
    items: list[str] = []
    for b in blockers:
        sev = (b.get("severity") or "?").lower()
        title = _esc(b.get("title", "(no title)"))
        reason = _esc(b.get("reason", ""))
        closures = b.get("closure_requirements") or []
        cl_html = (
            "<ul>" + "".join(f"<li>{_esc(c)}</li>" for c in closures) + "</ul>"
            if closures
            else ""
        )
        items.append(
            f'<div class="blocker"><strong>{_esc(sev.upper())}</strong> {title}'
            f' — {reason}{cl_html}</div>'
        )
    return (
        '<section class="report-section">'
        "<h2>Hard blockers</h2>"
        '<div class="hard-blockers">'
        f'<h3>{len(blockers)} blocker(s) — close before merge</h3>'
        + "".join(items)
        + "</div></section>"
    )


def _findings_table_html(findings: list[dict], *, table_id: str = "") -> str:
    if not findings:
        return '<p class="muted">No findings to display.</p>'
    rows: list[str] = []
    for f in findings:
        sev = (f.get("severity") or "info").lower()
        ev = (f.get("evidence") or [{}])[0]
        path = _esc(ev.get("path", "n/a"))
        line = ev.get("line_start")
        path_ref = f"{path}:{line}" if line else path
        rows.append(
            f'<tr class="sev-{_esc(sev)}">'
            f'<td data-sort="{_SEVERITY_RANK.get(sev, 0)}"><span class="sev-cell">{_esc(sev.upper())}</span></td>'
            f'<td>{_esc(f.get("category", "?"))}</td>'
            f'<td><code>{_esc(f.get("subcategory", "?"))}</code></td>'
            f'<td>{_esc(f.get("statement", ""))}</td>'
            f'<td><code>{_esc(path_ref)}</code></td>'
            f"</tr>"
        )
    return (
        f'<table class="data sortable" id="{_esc(table_id)}">\n'
        '  <thead><tr>'
        '<th>Severity</th><th>Category</th><th>Subcategory</th>'
        '<th>Statement</th><th>Location</th>'
        "</tr></thead>\n"
        f"  <tbody>{''.join(rows)}</tbody>\n"
        "</table>"
    )


def _category_scoring_table_html(category_scores: list[dict]) -> str:
    rows: list[str] = []
    for cat in category_scores:
        applicable = "yes" if cat.get("applicable") else "no"
        if not cat.get("applicable"):
            score_cell, max_cell = "—", "—"
        else:
            score_cell = _esc(cat.get("score", 0))
            max_cell = _esc(cat.get("max_score", 0))
        rows.append(
            "<tr>"
            f'<td>{_esc(cat.get("category", "?"))}</td>'
            f'<td>{applicable}</td>'
            f'<td data-sort="{cat.get("score", 0) if cat.get("applicable") else -1}">{score_cell}</td>'
            f'<td>{max_cell}</td>'
            f'<td>{_esc(cat.get("summary", ""))}</td>'
            "</tr>"
        )
    return (
        '<table class="data sortable">\n'
        '  <thead><tr><th>Category</th><th>Applicable</th><th>Score</th><th>Max</th><th>Summary</th></tr></thead>\n'
        f"  <tbody>{''.join(rows)}</tbody>\n"
        "</table>"
    )


def _findings_grouped_by_category_html(findings: list[dict]) -> str:
    if not findings:
        return '<p class="muted">No production findings to display.</p>'
    bucket: dict[str, list[dict]] = defaultdict(list)
    for f in findings:
        bucket[f.get("category", "unknown")].append(f)
    blocks: list[str] = []
    for cat in sorted(bucket.keys()):
        sorted_in_cat = sorted(bucket[cat], key=lambda f: _SEVERITY_ORDER.get(f.get("severity", "low"), 5))
        items: list[str] = []
        for f in sorted_in_cat:
            sev = (f.get("severity") or "info").lower()
            ev = (f.get("evidence") or [{}])[0]
            path = ev.get("path", "")
            line = ev.get("line_start")
            path_ref = f"{path}:{line}" if line else path
            items.append(
                f'<li class="sev-{_esc(sev)}">'
                f'<span class="sev-cell">{_esc(sev.upper())}</span> '
                f"{_esc(f.get('statement', ''))} — <code>{_esc(path_ref)}</code>"
                f"</li>"
            )
        blocks.append(f"<h3>{_esc(cat)}</h3><ul>{''.join(items)}</ul>")
    return "\n".join(blocks)


def render_html_report(scored: dict) -> str:
    repo_meta = scored.get("repo_meta", {}) or {}
    classification = scored.get("classification", {}) or {}
    scoring = scored.get("scoring", {}) or {}
    findings = scored.get("findings") or []
    inventory = scored.get("inventory", {}) or {}
    category_scores = _normalize_category_scores(scoring.get("category_scores", []))

    prod_findings, fixture_findings_list = _split_findings(findings)

    eff = scoring.get("effective_profile", {}) or {}
    use_case = eff.get("use_case")
    use_case_profile = _resolve_use_case_profile(use_case) or {}
    blocks = narrate_for_persona(scored, use_case_profile)

    page_title = f"SDLC Assessment — {_esc(repo_meta.get('name', 'unknown'))}"

    parts: list[str] = []
    parts.append("<!DOCTYPE html>")
    parts.append('<html lang="en">')
    parts.append("<head>")
    parts.append('<meta charset="utf-8">')
    parts.append(f"<title>{page_title}</title>")
    parts.append('<meta name="viewport" content="width=device-width, initial-scale=1">')
    parts.append(f"<style>{_STYLESHEET}</style>")
    parts.append("</head>")
    parts.append("<body>")
    parts.append('<div class="container">')

    # Header band
    parts.append('<header class="report-header">')
    parts.append(f"<h1>{page_title}</h1>")
    parts.append('<div class="meta">')
    parts.append(f'<span><strong>Use case</strong> {_esc(use_case or "n/a")}</span>')
    parts.append(
        f'<span><strong>Maturity</strong> {_esc(eff.get("maturity", "n/a"))}</span>'
    )
    parts.append(
        f'<span><strong>Repo type</strong> {_esc(eff.get("repo_type", "n/a"))}</span>'
    )
    parts.append(
        f'<span><strong>Branch</strong> <code>{_esc(repo_meta.get("default_branch", "unknown"))}</code></span>'
    )
    parts.append(
        f'<span><strong>Analyzed</strong> {_esc(repo_meta.get("analysis_timestamp", datetime.now(UTC).isoformat()))}</span>'
    )
    parts.append("</div>")
    parts.append("</header>")

    # Executive callout
    parts.append(_exec_callout_html(scored, blocks))

    # Scorecard
    parts.append(
        _scorecard_html(
            scored,
            prod_findings=len(prod_findings),
            fixture_count=len(fixture_findings_list),
        )
    )

    # Persona narrative sections
    if blocks:
        parts.append('<section class="report-section">')
        parts.append(
            f'<h2>Persona-aware narrative — <code>{_esc(use_case or "n/a")}</code></h2>'
        )
        parts.append(
            '<p class="muted">Generated from the use-case profile\'s narrative_emphasis terms.</p>'
        )
        for b in blocks:
            parts.append(_narrative_block_html(b))
        parts.append("</section>")

    # Hard blockers
    parts.append(_hard_blockers_html(scored))

    # Quantitative inventory
    parts.append('<section class="report-section">')
    parts.append("<h2>Quantitative inventory</h2>")
    parts.append('<dl class="kv">')
    for key, label in _INVENTORY_FIELDS:
        value = inventory.get(key)
        parts.append(
            f"<dt>{_esc(label)}</dt><dd>{_esc(value if value is not None else 'n/a')}</dd>"
        )
    # Surface a couple of classification fields useful in the inventory view.
    parts.append(
        f"<dt>Network exposure</dt><dd>{_esc(classification.get('network_exposure', 'n/a'))}</dd>"
    )
    parts.append(
        f"<dt>Release surface</dt><dd>{_esc(classification.get('release_surface', 'n/a'))}</dd>"
    )
    parts.append("</dl>")
    parts.append("</section>")

    # Category scoring matrix
    parts.append('<section class="report-section">')
    parts.append("<h2>Category scoring matrix</h2>")
    parts.append(_category_scoring_table_html(category_scores))
    parts.append("</section>")

    # Detailed findings — production-first, fixtures collapsed
    parts.append('<section class="report-section">')
    parts.append("<h2>Detailed findings</h2>")
    parts.append("<h3>Production findings (sortable)</h3>")
    ranked_prod = sorted(prod_findings, key=lambda f: -_finding_rank(f))
    parts.append(_findings_table_html(ranked_prod, table_id="prod-findings"))
    parts.append("<h3>Production findings by category</h3>")
    parts.append(_findings_grouped_by_category_html(prod_findings))
    if fixture_findings_list:
        parts.append(
            f'<details class="fixture-section">'
            f"<summary>Fixture / non-production findings "
            f"({len(fixture_findings_list)}) — click to expand</summary>"
            '<p class="muted">These findings come from <code>tests/fixtures/</code>, '
            "<code>examples/</code>, <code>vendor/</code> or similar non-production paths. "
            "They are real signals (the tool is detecting its own intentional fixtures) "
            "but should not be confused with findings in production source.</p>"
        )
        ranked_fix = sorted(fixture_findings_list, key=lambda f: -_finding_rank(f))
        parts.append(_findings_table_html(ranked_fix, table_id="fixture-findings"))
        parts.append("</details>")
    parts.append("</section>")

    # Evidence appendix
    parts.append('<section class="report-section">')
    parts.append("<h2>Evidence appendix</h2>")
    parts.append(
        f"<p>Total findings: <strong>{len(findings)}</strong> "
        f"({len(prod_findings)} production · {len(fixture_findings_list)} fixture-derived).</p>"
    )
    parts.append(
        f"<p>Hard blockers: <strong>{len(scored.get('hard_blockers') or [])}</strong>. "
        f"Detector packs registered: see <code>scoring.applied_weights</code> in the JSON artifact.</p>"
    )
    parts.append("</section>")

    parts.append("</div>")  # /.container
    parts.append(f"<script>{_SORT_SCRIPT}</script>")
    parts.append("</body></html>")
    return "\n".join(parts) + "\n"


